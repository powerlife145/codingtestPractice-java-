import java.util.Scanner;

public class BaekJoon_10869 {

    public static void main(String[] args) {
        //백준 10869번 문제
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        int b = sc.nextInt();

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
